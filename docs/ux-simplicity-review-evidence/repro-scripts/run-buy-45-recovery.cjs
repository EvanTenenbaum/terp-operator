const { launch, snap, nukeOverlay, readToasts, aria } = require('./buy-lib.cjs');
(async () => {
  const { page, done } = await launch('intake@terpagro.local');
  const toastLog = [];
  setInterval(async () => { try { (await readToasts(page)).forEach(t => { if (!toastLog.includes(t)) toastLog.push(t); }); } catch {} }, 300);
  await page.goto('http://localhost:5173/intake');
  await page.waitForTimeout(2500);
  await nukeOverlay(page);
  const queue = page.getByRole('region', { name: 'Intake queue' });
  const poRow = queue.locator('.ag-pinned-left-cols-container .ag-row').filter({ hasText: 'PO-ACTIVE-008' }).first();
  await poRow.locator('.ag-group-contracted').click();
  await page.waitForTimeout(1800);
  const fw = page.locator('.ag-full-width-container');
  const idx2 = await page.evaluate(() => [...document.querySelectorAll('.ag-full-width-container .ag-pinned-left-cols-container .ag-row')].find(x => x.innerText.includes('MQBNAI3X-155'))?.getAttribute('row-index'));
  const hist = fw.locator(`.ag-pinned-right-cols-container .ag-row[row-index="${idx2}"]`).getByRole('button', { name: /History/ });
  console.log('history btn:', await hist.count(), JSON.stringify(await hist.allInnerTexts().catch(() => [])));
  await hist.click();
  await page.waitForTimeout(2500);
  console.log('URL:', page.url());
  await snap(page, '45-recovery-landing');
  const main = await aria(page, 'main');
  console.log(main.slice(0, 6000));
  console.log('ALL TOASTS:', JSON.stringify(toastLog, null, 1));
  await done();
  process.exit(0);
})().catch(e => { console.error(e); process.exit(1); });
